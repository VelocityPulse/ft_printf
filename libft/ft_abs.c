#include "libft.h"

int			ft_abs(int a)
{
	if (a == -2147483648)
		return (2147483648);
	return (a < 0 ? -a : a);
}

long long	ft_absll(long long a)
{
	if (a == -9223372036854775807)
		return (9223372036854775807);
	return (a < 0 ? -a : a);
}

double		ft_absd(double a)
{
	return (a < 0 ? -a : a);
}
