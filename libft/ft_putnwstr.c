#include "libft.h"

int		ft_putnwstr(int *str, int len)
{
	int		ret;
	int		i;

	ret = 0;
	i = -1;
	while (ret < len)
		ret += ft_putnwchar(str[++i], len - ret);
	return (ret);
}
